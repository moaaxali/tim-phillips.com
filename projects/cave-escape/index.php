    <h3>Cave Escape (2012)</h3>

    <p><b>Cave Escape</b> is a mobile game that was developed as part of the third <a href="http://greatcanadianappathon.com/" target="_blank">Great Canadian Appathon</a>, a 48-hour game development competition across all Canadian universities. The theme this year was 'Retro'. Together with two friends, my team worked through a weekend to create a side-scrolling adventure game. </p>
   
    <p id="instructions_box"><i>Run! Run! How long can you survive?<br><br>
    Your mine is collapsing and you must blast your way to safety. Plant dynamite to destroy rocks in your path, but be careful not to blow yourself up too! Remember, not all rocks are destructible.<br><br>
    Will you be tempted by the glitter of gold? Destroy gold laced rocks and pick up their coins to boost your score.<br><br>
    Hurry! Time is running out! Move by touching the screen in the direction you want to go.</i>
    </p>
    
