    <p><b>Our pitch:</b></p>
    
    <p>As the City of Edmonton grows and expands, it becomes harder and harder to keep track of all the great events and attraction our rich community has to offer. Before the inception of our product, a potential traveler might waste much of their time consulting other Edmontonians or surfing a few scattered web resources on what to do during their stay here. For individuals on frequent business trips such as yourselves, this problem is compounded due to the fact that you are on a tight schedule. Most of your time is spent at conferences or meetings and the only time you have to enjoy our fine city is scattered among a few time slots during the day.</p>
    
    <p>Our application is designed to help you make the most of your time during those limited periods of downtime. Personal Edmonton Planner provides an all encompassing approach to enjoying what Edmonton has to offer in the simplest and most intuitive way possible. At just a touch of a button, the application's advanced algorithm will suggest an ideal way for you to spend your afternoon or instantly locate your favourite restaurant. Every suggestion is tailored exclusively to your personal profile preferences - based on what you enjoy. The wide selection of sources which our application queries will never leave you idle. However, if you do decide that none of the suggestions provided are particularly appealing for a particular time slot, you can manually search through our comprehensive, yet easy to use, catalog of events and places.</p>
    
    <p>Finding entertainment in an unfamiliar city is half the challenge and Personal Edmonton Planner already allows to do this with the click of a mouse. Navigating your way around is the other half and can be very difficult for new visitors. Our application also allows you to get to and from your destinations with ease. Through the direct integration with the Edmonton Transit System, the map interface allows for dynamic route mapping based on your current location and that of your destination.</p>
    
    <p>With PEP, you can create a great schedule for yourself a day or even a week in advance, just in a matter of minutes. Whether you plan to dine in that Asian takeout place around the corner, or visit Edmonton downtown library, you will always know how to get there and back in time for your meeting.</p>
    
